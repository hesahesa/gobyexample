# Untuk menjalankan program, letakkan kode di file `hello-world.go` dan
# gunakan `go run`.
$ go run hello-world.go
hello world

# Terkadang kita ingin mem-build program kita ke sebuah
# binary. Kita bisa melakukan ini dengan `go build`.
$ go build hello-world.go
$ ls
hello-world	hello-world.go

# Kita lalu bisa menjalankan binary yang telah di-build secara langsung.
$ ./hello-world
hello world

# Sekarang kita telah bisa menjalankan dan mem-build program Go sederhana, mari
# belajar lebih lanjut tentang bahasa ini.
